# Check if DB tunnel is running
check_db_tunnel() {
    echo -e "${CYAN}â†’ Checking database tunnel...${NC}"

    if pgrep -f "start-db-tunnel.sh" > /dev/null || pgrep -f "AWS-StartPortForwardingSessionToRemoteHost" > /dev/null; then
        echo -e "${GREEN}  âœ“ Database tunnel is running${NC}"
        echo ""
        return 0
    else
        echo -e "${YELLOW}  âš  Database tunnel not running${NC}"
        echo -e "${CYAN}  â†’ Starting database tunnel...${NC}"

        "${PROJECT_ROOT}/scripts/dev/start-db-tunnel.sh" > "${LOG_DIR}/batbern-dev-db-tunnel.log" 2>&1 &
        local tunnel_pid=$!
        echo $tunnel_pid > "${PID_DIR}/batbern-dev-db-tunnel.pid"
        disown  # Remove from job control so parent script can exit

        echo -e "${CYAN}  â†’ Waiting for tunnel to be ready (15 seconds)...${NC}"
        sleep 15

        if ps -p $tunnel_pid > /dev/null 2>&1; then
            echo -e "${GREEN}  âœ“ Database tunnel started (PID: $tunnel_pid)${NC}"
        else
            echo -e "${RED}  âœ— Database tunnel failed to start${NC}"
            echo "    Check logs: cat ${LOG_DIR}/batbern-dev-db-tunnel.log"
            exit 1
        fi
        echo ""
    fi
}

