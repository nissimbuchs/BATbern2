# Check prerequisites
check_prerequisites() {
    echo -e "${CYAN}â†’ Checking prerequisites...${NC}"

    local errors=0

    # Check Java
    if ! command -v java &> /dev/null; then
        echo -e "${RED}  âœ— Java not found${NC}"
        echo "    Install: https://adoptium.net/ (Java 21 required)"
        errors=$((errors + 1))
    else
        JAVA_VERSION=$(java -version 2>&1 | head -n 1 | awk -F '"' '{print $2}' | cut -d'.' -f1)
        if [ "$JAVA_VERSION" -lt 21 ]; then
            echo -e "${RED}  âœ— Java version $JAVA_VERSION found, but Java 21+ required${NC}"
            errors=$((errors + 1))
        else
            echo -e "${GREEN}  âœ“ Java $JAVA_VERSION${NC}"
        fi
    fi

    # Check Node.js
    if ! command -v node &> /dev/null; then
        echo -e "${RED}  âœ— Node.js not found${NC}"
        echo "    Install: https://nodejs.org/ (Node.js 20+ required)"
        errors=$((errors + 1))
    else
        NODE_VERSION=$(node -v | cut -d'v' -f2 | cut -d'.' -f1)
        if [ "$NODE_VERSION" -lt 20 ]; then
            echo -e "${RED}  âœ— Node.js version $NODE_VERSION found, but Node.js 20+ required${NC}"
            errors=$((errors + 1))
        else
            echo -e "${GREEN}  âœ“ Node.js $(node -v)${NC}"
        fi
    fi

    # Check Gradle
    if ! command -v gradle &> /dev/null; then
        echo -e "${YELLOW}  âš  Gradle not found (will use ./gradlew)${NC}"
    else
        echo -e "${GREEN}  âœ“ Gradle $(gradle -v | grep Gradle | awk '{print $2}')${NC}"
    fi

    # Check npm
    if ! command -v npm &> /dev/null; then
        echo -e "${RED}  âœ— npm not found${NC}"
        errors=$((errors + 1))
    else
        echo -e "${GREEN}  âœ“ npm $(npm -v)${NC}"
    fi

    # Check .env file
    if [ ! -f "${ENV_FILE}" ]; then
        echo -e "${RED}  âœ— .env file not found${NC}"
        echo "    Run: ./scripts/config/sync-backend-config.sh development"
        errors=$((errors + 1))
    else
        echo -e "${GREEN}  âœ“ .env file exists${NC}"
    fi

    echo ""

    if [ $errors -gt 0 ]; then
        echo -e "${RED}Prerequisites check failed. Please fix the errors above.${NC}"
        exit 1
    fi
}

