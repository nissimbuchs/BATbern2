	<div class="text">

F&uuml;r viele Unternehmen ist ihre Informatik zunehmend ein kritischer Produktionsfaktor. Deshalb ist die Fragestellung wichtig, wie diese Informatik
finanziell und inhaltlich gesteuert werden kann. Die Steuerung muss sich sowohl mit der bestehenden Situation wie auch deren Ver&auml;nderung befassen.
Betroffen sind also unter anderem Anwendungen als bestehende Verm&ouml;genswerte, wie auch Projekte als Mittel zur Ver&auml;nderung dieser Anwendungen.
Wie eine solche Steuerung zweckm&auml;ssig organisiert wird und welchen Beitrag eine Unternehmensarchitektur dazu leisten kann, sind also zentrale Fragen
der Unternehmensf&uuml;hrung.

Das 23. Berner Architekten Treffen war diesen Fragen gewidmet. Vertreterinnen und Vertreter von Berner Firmen haben  aufgezeigt, wie sie das Thema
"Management von Projekt- und Anwendungsportfolios" angehen und umsetzen.

