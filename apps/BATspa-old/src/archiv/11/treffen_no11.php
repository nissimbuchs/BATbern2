	<div class="text">
		Das elfte Berner Architekten Treffen fand im gepflegten Rahmen des Zentrum Paul Klee zum Thema "Workplace" statt. Den 110 teilnehmenden IT-Fachpersonen wurden vier Vortr&auml;ge pr&auml;sentiert, welche die Spannweite des Themas deutlich vor Augen f&uuml;hrte. Im ersten Vortrag zeigte Ernst Kammermann-Gerber von der SBB IT, welche Treiber die Gestaltung der n&auml;chsten Generation des IT-Arbeitsplatzes in einem Grosskonzern bestimmen. Der zweite Vortrag von Rudolf Oppliger von der PostFinance demonstrierte, wie sich der auf einer Rich-Client-Plattform basierte Spezialisten-Arbeitsplatz in den letzten dreieinhalb Jahren weiterentwickelt hat und welche Vision und Strategie die Grundlage daf&uuml;r bildet. Anschliessend beschrieb Andreas R. Lehmann von den Swisscom IT Services, welche Dienste ein Wissensarbeiter zuk&uuml;nftig nutzen wird, um seine Anforderungen an Unabh&auml;ngigkeit von Arbeitsort, Arbeitszeit und Arbeitsweise abzudecken. Der abschliessenden Vortrag hielt Mauro Sala vom RTC zur Entwicklung des Arbeitsplatzes im Bankenumfeld und den sich stellenden Anforderungen beim Management eines solchen Arbeitsplatzes.
		<p>
		Der anschliessende Ap&eacute;ro bot wiederum die Gelegenheit zu vertieften Diskussionen und Networking unter Gleichgesinnten in angenehmer und architektonisch eindr&uuml;cklicher Umgebung.
