Die ICT besitzt in heutigen Gesch&auml;ftsmodellen eine stark unterschiedliche Bedeutung:
<ul>
<li>In klassischen Modellen stellt sie eine Routine-Dienstleistung dar, die f&uuml;r sich alleine weder zur Strategie noch zur Produktivit&auml;t einer Unternehmung einen relevanten Beitrag leistet.
