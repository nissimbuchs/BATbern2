					Container zur Virtualisierung von Applikationen<br>
					In den letzten Jahren hat die Virtualisierung auf der Ebene Betriebssystem sehr grosse Verbreitung gefunden. 
					Applikationen k&ouml;nnen damit ressourcensparend und trotzdem isoliert ausgef&uuml;hrt werden.
					Aus der Verwendung von Container ergeben sich m&auml;chtige M&ouml;glichkeiten beispielsweise zur Standardisierung 
					von Anwendungsumgebungen, zur Automatisierung von Deployments, zur Skalierung von Applikationen und zum 
					Einsatz neuer Software-Architekturen.
					Allgemein f&ouml;rdern Container den Einsatz unterschiedlicher Formen von Cloud Computing.
