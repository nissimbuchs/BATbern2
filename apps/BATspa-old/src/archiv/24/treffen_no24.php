	<p>
	<div class="text">
		Als "Open Source Software" wird Software bezeichnet, deren Code frei verf&uuml;gbar ist. Die wesentliche Eigenschaft von Open Source Software ist dabei nicht, dass die Software gratis verf&uuml;gbar ist sondern dass sie frei verwendet, kopiert, weitergegeben, analysiert und modifiziert werden darf. Der Pionier von "Free Software", Richard Stallman vergleicht dies mit dem Unterschied zwischen Freibier und dem grundlegenden Recht der Redefreiheit. Zunehmend setzen heute auch Firmen und staatliche Organisationen Open Source Software ein. Dabei interessieren einerseits die daraus entstehenden Vorteile, also ob aus den "Freiheitsrechten" abgeleiteter Nutzen und Kosteneinsparungen erzielt werden k&ouml;nnen. Andererseits interessiert auch die Frage nach entstehenden Nachteilen, wie fehlendem Support oder fehlender Haftung und wie diese Nachteile gemindert werden k&ouml;nnen.
		<p>
		Das 24. Berner Architekten Treffen war diesen Fragen gewidmet. Vertreterinnen und Vertreter von Berner Firmen haben aufgezeigt, wie sie das Thema "Open Source Software" angehen und umsetzen.
