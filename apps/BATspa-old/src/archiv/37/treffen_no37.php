					In den letzten Jahren haben sich jedoch neue, leichtgewichtigere Konzepte entwickelt, die auf der Java Runtime Umgebung 
					lauff&auml;hig sind. Mit JavaScript hat sich ausserdem ein Standard auf dem Web Frontend herausgebildet.
