	<div class="ueberschrift">Das 25. BAT Architekturforum im R&uuml;ckblick</div>
	<p>
	<div class="text">
		F&uuml;r Unternehmen stellt sich zunehmend die Frage, welche IT-Leistungen sie selbst erbringen und welche sie am Markt beziehen wollen: Einerseits besteht der operative Druck nach Effizienzsteigerungen, andererseits wird das Leistungsangebot des Markts breiter und differenzierter. Es erstreckt sich &uuml;ber den ganzen Lebenszyklus einer IT-Landschaft von der Planung &uuml;ber die Beschaffung von Applikationen und Plattformen bis zu deren Betrieb (z.B. SaaS, Paas, Iaas).
		<p>
		Ein Unternehmen mit langfristigem Fokus muss neben der operativen auch die strategische Sicht einbeziehen: Welche Kompetenzen sind f&uuml;r die Zukunft des Unternehmens wichtig - beispielsweise um Marktleistungen gezielt einzukaufen, eine Unternehmensplanung effektiv durchzuf&uuml;hren oder das Risiko von unerw&uuml;nschten Abh&auml;ngigkeiten zu reduzieren - und welche dieser Kompetenzen gehen mittelfristig verloren, wenn eine Leistung nicht mehr selbst erbracht wird.
		<p>
		An diesem BAT m&ouml;chten wir sowohl operative wie auch strategische Aspekte ausloten und erfahren, wie Unternehmen die Frage konkret angehen.
