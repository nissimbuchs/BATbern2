	<div class="text">
		Das neunte Berner Architekten Treffen fand zum ersten Mal im architektonisch und kulturell sehr ansprechenden Ambiente des Zentrums Paul Klee statt. Das Thema Business Rules vermochte &uuml;ber 120 Teilnehmer zu mobilisieren und demonstrierte dadurch eindr&uuml;cklich die Anziehungskraft des Themas und der Veranstaltung. Unser Gastreferent aus Deutschland f&uuml;hrte anhand der Open Source Rule Engine Drools ins Thema ein und zeigte aufgrund des regen Interesses auch w&auml;hrend der Veranstaltungspause einige Beispiele mit Drools. Die anschliessenden Praxisbeitr&auml;ge aus Berner Firmen zeigten sowohl die Probleme im Umgang mit Business Rules und mit der Akzeptanz des Ansatzes als auch Beispiele gelungener Umsetzungen auf. Das Beispiel der PostFinance ist unseres Erachtens besonders erw&auml;hnenswert, da es sich hierbei um Gesch&auml;ftsregeln handelt, die ausschliesslich von der Fachabteilung erstellt und gepflegt werden, ohne Hinzutun der IT-Abteilung. W&auml;hrend des tradtionellen abschliessenden Ap&eacute;ros ergab sich wiederum die Gelegenheit zu vertieften Diskussionen und Networking unter Gleichgesinnten.
