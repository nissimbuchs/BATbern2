	<div class="ueberschrift">Das 22. BAT Architekturforum im R&uuml;ckblick</div>
	<p>
	<div class="text">

Mobilkommunikation spielt in Unternehmen eine immer gr&ouml;ssere Rolle und stellt diese durch ihre hohe Vielfalt und Ver&auml;nderungsgeschwindigkeit vor grosse Herausforderungen. Wichtige Aspekte sind unter anderen:
<ul>
<li>Welche Technologien werden eingesetzt und wie werden sie verwaltet? Welche Technologien werden dadurch ersetzt?</li>
<li>Welche neuen Kollaborationsm&ouml;glichkeiten ergeben sich und wie k&ouml;nnen diese sinnvoll genutzt werden?</li>
<li>Welche neuen Herausforderungen stellen sich bez&uuml;glich Sicherheit?</li>
<li>Wie wird mit der zunehmenden "Consumerization", also der Aufl&ouml;sung der Grenze zwischen Berufs- und Privatleben, umgegangen?</li>
</ul>
Das BAT Architekturforum ist diesen Fragen gewidmet. Vertreterinnen und Vertreter von Berner Firmen werden aufzeigen, wie sie das Thema "Mobilkommunikation im Unternehmen" angehen und umsetzen.

