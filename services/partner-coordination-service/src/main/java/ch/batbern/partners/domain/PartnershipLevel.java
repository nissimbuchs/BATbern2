package ch.batbern.partners.domain;

/**
 * Partnership tier levels for BATbern partner companies.
 *
 * Each level represents different benefits and engagement opportunities:
 * - BRONZE: Entry-level partnership
 * - SILVER: Standard partnership with moderate benefits
 * - GOLD: Premium partnership with enhanced benefits
 * - PLATINUM: Elite partnership with comprehensive benefits
 * - STRATEGIC: Long-term strategic partnership with maximum benefits
 *
 * Partnership level affects:
 * - Topic voting weight
 * - Meeting invitations
 * - Logo placement and visibility
 * - Speaking opportunities
 */
public enum PartnershipLevel {
    BRONZE,
    SILVER,
    GOLD,
    PLATINUM,
    STRATEGIC
}
